bool Oled::init() {
    Wire.setSDA(PIN_OLED_SDA);
    Wire.setSCL(PIN_OLED_SCL);
    Wire.begin();

    if (!display.begin(SSD1306_SWITCHCAPVCC, OLED_I2C_ADDR)) {
        Serial.println("[OLED] FAIL - SSD1306 not found");
        return false;
    }

    return true;
}

void Oled::show_image(bool show) {
    display.clearDisplay();
    if (show) {
        display.drawBitmap(0, 0, chippy, 128, 64, SSD1306_WHITE);
    }
    display.display();
}

void Oled::show_button(const char* button_name) {
	display.clearDisplay();

	display.setTextColor(SSD1306_WHITE);
	display.setTextSize(1);
	display.setCursor(0, 0);
	display.println("Button:");

	display.setTextSize(2);
	display.setCursor(0, 24);
	if (button_name != nullptr) {
		display.println(button_name);
	} else {
		display.println("NONE");
	}

	display.display();
}
